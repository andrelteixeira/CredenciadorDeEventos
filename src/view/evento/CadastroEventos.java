    public CadastroEventos(int codigo) throws ParseException {
        initComponents();
        if (codigo == 0) {
            lb_codigo.setText(String.valueOf(codigo));
        } else {
            ControlEvento controle = new ControlEvento();
            vetor = controle.recuperar(codigo);

            lb_codigo.setText(vetor[0]);
            lb_nome.setText(vetor[1]);
            lb_descricao.setText(vetor[2]);
            lb_capacidade.setText(vetor[3]);
            lb_endereco.setText(vetor[4]);
            lb_cidade.setText(vetor[5]);
            
            SimpleDateFormat formatSpinner = new SimpleDateFormat("EEE MMM dd HH:mm:ss zzz yyyy", Locale.US);
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
           
            sp_inicial.setValue(formatSpinner.parse(formatSpinner.format(dateFormat.parse(vetor[6]))));
            sp_final.setValue(formatSpinner.parse(formatSpinner.format(dateFormat.parse(vetor[7]))));
        }
        lb_codigo.setEditable(false);
    }

