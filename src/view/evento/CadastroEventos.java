    private void jMenu1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jMenu1MouseClicked
        ControlEvento ctrl = new ControlEvento();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        if (!lb_codigo.getText().equals("")
                && !lb_nome.getText().equals("")
                && !sp_inicial.getValue().toString().equals("")
                && !sp_inicial.getValue().toString().equals("")) {

            vetor[0] = lb_codigo.getText();
            vetor[1] = lb_nome.getText();
            vetor[2] = lb_descricao.getText();
            vetor[3] = lb_capacidade.getText();
            vetor[4] = lb_endereco.getText();
            vetor[5] = lb_cidade.getText();
            try {
                SimpleDateFormat formatSpinner = new SimpleDateFormat("EEE MMM dd HH:mm:ss zzz yyyy", Locale.US);
                
                vetor[6] = sdf.format(formatSpinner.parse(sp_inicial.getValue().toString()));
                vetor[7] = sdf.format(formatSpinner.parse(sp_final.getValue().toString()));
            } catch (ParseException ex) {
                Logger.getLogger(CadastroEventos.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (vetor[8] == null) {
                vetor[8] = sdf.format(Calendar.getInstance().getTime());
            }

            ctrl.salvar(vetor);
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Por Favor, Preencha todos os campos!");
        }
    }//GEN-LAST:event_jMenu1MouseClicked

