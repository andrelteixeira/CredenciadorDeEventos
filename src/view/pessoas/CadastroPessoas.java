    public CadastroPessoas(int codigo) {
        initComponents();
       
        if (codigo == 0) {
            lb_codigo.setText(String.valueOf(codigo));
            DateFormat dateFormat = new SimpleDateFormat("yyyyMMddHHmmss");
            Date date = new Date();
            lb_matricula.setText(dateFormat.format(date));
        } else {
            ControlPessoa controle = new ControlPessoa();
            vetor = controle.recuperar(codigo);

            lb_codigo.setText(vetor[0]);
            lb_nome.setText(vetor[1]);
            lb_cpf.setText(vetor[2]);
            lb_rg.setText(vetor[3]);
            lb_endereco.setText(vetor[4]);
            lb_cidade.setText(vetor[5]);
            lb_telefone.setText(vetor[6]);
            lb_email.setText(vetor[7]);
            lb_matricula.setText(vetor[8]);
        }
        lb_codigo.setEditable(false);
        lb_matricula.setEditable(false);
    }

