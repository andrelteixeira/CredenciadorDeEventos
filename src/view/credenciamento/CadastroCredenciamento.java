    private void bt_part_proximoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_part_proximoActionPerformed
        String msg = "";
        try {

            ControlInscricaoEvento control = new ControlInscricaoEvento();
            String[] credenciamento = new String[6];
            credenciamento[0] = "0";
            credenciamento[1] = eventoCadastro[0];
            credenciamento[2] = pessoaCadastro[0];
            credenciamento[3] = usuario[0];
            credenciamento[4] = String.valueOf(false);
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
            credenciamento[5] = sdf.format(Calendar.getInstance().getTime());

            control.salvar(credenciamento);
            msg = "usuario inscrito com sucesso!";
        } catch (Exception e) {
            msg = "Usuario não cadastrado";
            Logger.getLogger(CadastroCredenciamento.class.getName()).log(Level.SEVERE, null, e);
        }
        tela = tela + 1;
        jTabbedPane1.setEnabledAt(tela, true);
        label_texto.setText(msg.toUpperCase());
        jTabbedPane1.setSelectedIndex(tela);
    }//GEN-LAST:event_bt_part_proximoActionPerformed

