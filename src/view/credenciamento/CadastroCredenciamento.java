    public void preencheTelaEvento(int codigo) {
        try {
            ControlEvento controle = new ControlEvento();
            eventoCadastro = controle.recuperar(codigo);

            if (!eventoCadastro[4].equals("0")) {

                lb_evento.setText(eventoCadastro[1]);
                lb_abertas.setText(eventoCadastro[3]);
                lb_disponivel.setText("");
                SimpleDateFormat formatSpinner = new SimpleDateFormat("EEE MMM dd HH:mm:ss zzz yyyy", Locale.US);
                SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
                sp_inicial.setValue(formatSpinner.parse(formatSpinner.format(dateFormat.parse(eventoCadastro[6]))));
                sp_final.setValue(formatSpinner.parse(formatSpinner.format(dateFormat.parse(eventoCadastro[7]))));
                bt_evnt_proximo.setEnabled(true);

                jTabbedPane1.setSelectedIndex(0);
            } else {
                JOptionPane.showMessageDialog(null, "Envento com todas as vagas ocupadas.\nPor favor escolha outro evento!!");
            }
        } catch (ParseException ex) {
            Logger.getLogger(CadastroCredenciamento.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
