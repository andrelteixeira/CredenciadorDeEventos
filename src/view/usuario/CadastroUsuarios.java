    private void jMenu1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jMenu1MouseClicked
        ControlUsuario ctrl = new ControlUsuario();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        try {
            if (!lb_codigo.getText().equals("")
                    && !lb_nome.getText().equals("")
                    && !lb_login.getText().equals("")
                    && !String.valueOf(pss_senha.getPassword()).equals("")) {

                vetor[0] = lb_codigo.getText();
                vetor[1] = lb_nome.getText();
                vetor[2] = lb_login.getText();
                vetor[3] = Criptografia.criptografa(String.copyValueOf(pss_senha.getPassword()));
                vetor[4] = String.valueOf(cb_admin.isSelected());
                if (vetor[5] == null) {
                    vetor[5] = sdf.format(Calendar.getInstance().getTime());
                }
                ctrl.salvar(vetor);
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Por Favor, Preencha todos os campos!");
            }
        } catch (NoSuchAlgorithmException | UnsupportedEncodingException | SQLException ex) {
            Logger.getLogger(CadastroUsuarios.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jMenu1MouseClicked

