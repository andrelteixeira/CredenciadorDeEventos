    public CadastroUsuarios(int codigo, String[] dados) {
        usuario = dados;
        initComponents();
        if (codigo == 0) {
            lb_codigo.setText(String.valueOf(codigo));
        } else {
            ControlUsuario controle = new ControlUsuario();
            vetor = controle.recuperar(codigo);
            lb_codigo.setText(vetor[0]);
            lb_nome.setText(vetor[1]);
            lb_login.setText(vetor[2]);
            cb_admin.setSelected(Boolean.parseBoolean(vetor[4]));
            if (!usuario[1].equals("true")){
                trocaHabilitar(false);
            }
        }        
        lb_codigo.setEnabled(false);
    }

    public void trocaHabilitar(boolean chave) {
        lb_login.setEnabled(chave);
        cb_admin.setEnabled(chave);
        DELETAR.setVisible(chave);
        
    }

